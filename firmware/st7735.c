//向液晶屏写一个8位指令
void Lcd_WriteIndex(uint8_t Index)
{
   //SPI 写命令时序开始
   spi_set_ss(0);
   GPIO->CLR=TFT_A0_Pin;
   spi_write_byte(Index);
   spi_set_ss(1);
}

//向液晶屏写一个8位数据
void Lcd_WriteData(uint8_t Data)
{
   spi_set_ss(0);
   GPIO->SET=TFT_A0_Pin;
   spi_write_byte(Data);
   spi_set_ss(1); 
}
//向液晶屏写一个16位数据
void LCD_WriteData_16Bit(uint16_t Data)
{
   spi_set_ss(0);
   GPIO->SET=TFT_A0_Pin;
	spi_write_byte(Data>>8); 	//写入高8位数据
	spi_write_byte(Data); 			//写入低8位数据
   spi_set_ss(1); 
}

void Lcd_WriteReg(uint8_t Index,uint8_t Data)
{
	Lcd_WriteIndex(Index);
 	Lcd_WriteData(Data);
}


void Lcd_Reset(void)
{
	GPIO->CLR=TFT_RESET_Pin;
	delay_ms(100);
	GPIO->SET=TFT_RESET_Pin;
	delay_ms(50);
}

